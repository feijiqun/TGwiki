<template><div><h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>顾名思义，邮箱登录就是通过电子邮件获取验证码来登录Telegram账户，此功能可以在把验证码发送到Telegram客户端的情况下同时向邮箱发送一份验证码，而不是通过短信发送验证码，极大减少了Telegram官方的短信费用支出。对于使用接码的用户来说，再也不怕没客户端也没手机号的情况下收不到验证码了！</p>
<h2 id="开启邮箱登录" tabindex="-1"><a class="header-anchor" href="#开启邮箱登录"><span>开启邮箱登录</span></a></h2>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>新注册的部分账号会默认开启邮箱登录，无需手动开启。</p>
<p>+888匿名号码或Telegram Premium用户无法开启邮箱登录！</p>
</div>
<p>根据官方文档来看，当你的登录频率足够高时，Telegram服务器会要求客户端设置邮箱登录。简单说就是让Durov认为你在浪费他的短信钱。</p>
<p><strong>具体方法：</strong></p>
<ol>
<li>
<p>使用Telegram官方移动客户端登录，登录时选择<code v-pre>使用短信验证码登录</code>（<code v-pre>Tap to get a code via SMS</code>）</p>
</li>
<li>
<p>短信验证码和Telegram客户端验证码一致，任选其一输入即可，<strong>不要输入2FA密码</strong>，直接返回。</p>
</li>
<li>
<p>重新上面的步骤，刷短信验证码。</p>
</li>
<li>
<p>如果过程中出现<code v-pre>尝试次数过多，请稍后再试</code>（<code v-pre>Too many attemps, please try again later</code>），则需要休息一段时间（通常是24小时）再刷。</p>
</li>
<li>
<p>当页面出现<code v-pre>选择登录邮箱</code>（<code v-pre>Choose a login email</code>），根据提示输入完成邮箱验证码后会向你的账号绑定手机号码发送短信验证码，输入完即可开启邮箱登录。</p>
<div class="hint-container warning">
<p class="hint-container-title">注意</p>
<p>此步骤中的验证码只能以短信的形式发送，验证码不会发送到已登录的客户端！也就是说，如果您想要开启邮箱登录，您绑定的号码需要可以接收短信验证码。</p>
</div>
