<h2 id="注意" tabindex="-1"><a class="header-anchor" href="#注意"><span>注意</span></a></h2>
<ul>
<li>
<p>+888匿名号码或Telegram Premium用户无法开启邮箱登录。</p>
</li>
<li>
<p>开启邮箱登录以后通过手机验证码重置邮箱需要开通大会员（888匿名号码除外）。</p>
</li>
<li>
<p>任何人知道知道你的手机号就能请求重置邮箱登录（需要等待7天）。重置邮箱登录之后你的账号会回归为未设置邮箱登录的状态。</p>
