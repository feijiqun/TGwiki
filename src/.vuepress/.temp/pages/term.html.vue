<h2 id="限制、冻结、封禁" tabindex="-1"><a class="header-anchor" href="#限制、冻结、封禁"><span>限制、冻结、封禁</span></a></h2>
<p>本条目的术语针对Telegram账号。</p>
<ol>
<li>
<p>限制</p>
<p>Telegram账号被官方限制使用，包括无法主动私聊、无法在公开群组中发送消息。</p>
</li>
<li>
<p>冻结</p>
<p>由于违反服务协议，账号被官方禁止使用，称为“冻结”。账号被冻结后会显示为<code v-pre>已注销账号</code>，并有一个雪花图标。冻结状态下不能进行任何操作，需申诉解封后才能继续使用Telegram。</p>
<div class="hint-container note">
<p class="hint-container-title">注</p>
<p>账号冻结于2025年4月推出，在此之前账号会被直接封禁，并在登录时提示<code v-pre>This phone number is banned</code>。</p>
</div>
<div class="hint-container info">
<p class="hint-container-title">相关信息</p>
<p>有关“冻结”的更多信息请参考 <RouteLink to="/frozen.html">此处</RouteLink> 。</p>
</div>
</li>
<li>
<p>封禁</p>
<p>由于频繁操作而被系统风控导致直接销号。号码被封禁后将无法登录账号，也无法注销。</p>
</li>
</ol>
<h2 id="封禁、拉黑、禁言" tabindex="-1"><a class="header-anchor" href="#封禁、拉黑、禁言"><span>封禁、拉黑、禁言</span></a></h2>
<p>本条目的术语针对Telegram群组内操作。</p>
<ol>
<li>
<p>封禁（ban）</p>
<ul>
<li>群组或频道的管理员（或有管理员权限的机器人）可以ban成员。</li>
<li>被群组或频道封禁，被群组或频道的管理员拉入群组或频道的黑名单了，也就禁止你的账号再加入群组或频道了。只能管理员取消封禁（拉黑），你才能再加入群组或频道。</li>
<li>如果你是用频道身份在群里发言的，群组管理员可以ban了你的频道，这样你在这个群就再也不能用任何频道身份发言了（其他频道也不行），在其他群里可以；不过这只能ban你的频道，不能ban了你的个人账号。</li>
</ul>
</li>
<li>
<p>拉黑（block）</p>
<ul>
<li>拉黑是指屏蔽某一个账号，通常在私聊中使用。拉黑后，你将不再收到对方的私聊消息，对方将无法查看你的头像和上线时间。</li>
<li>你可以在<code v-pre>设置</code>-&gt;<code v-pre>隐私</code>-&gt;<code v-pre>拉黑用户</code>中查看已被拉黑的账号并取消拉黑。</li>
</ul>
</li>
<li>
<p>禁言（mute）</p>
<ul>
<li>群组管理员（或有管理员权限的机器人）可以禁言成员。</li>
<li>被禁言后，你依然还在群里，也能看到大家发的消息，只是你无法发消息而已，没有被踢出群。</li>
<li>禁言可以选时间：x小时/x天/自定义/永久；如果有时间的，可以等到时间结束了就可以自动解除禁言了；如果是永久的，就只能找群管理员取消禁言了。</li>
</ul>
</li>
<li>
<p>全员禁言</p>
<p>群组管理员（或有管理员权限的机器人）可以在群组设置中关闭成员的&quot;发送消息&quot;权限，所有成员都不能发送消息，只有群管理员能发消息</p>
</li>
</ol>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>被封禁、被禁言或全员禁言，TG客户端界面都有相应的内容显示。</p>
