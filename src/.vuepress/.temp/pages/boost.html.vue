<h2 id="如何助推" tabindex="-1"><a class="header-anchor" href="#如何助推"><span>如何助推</span></a></h2>
<ul>
<li>
<p>助推链接的格式是<code v-pre>https://t.me/boost/</code>+<code v-pre>你的频道/群组链接</code>。</p>
</li>
<li>
<p>频道/群组管理员可以在 <code v-pre>更多</code>-&gt;<code v-pre>统计</code>-&gt;<code v-pre>助推</code> 中查看助推数据。</p>
</li>
<li>
<p>仅大会员（Telegram Premium）可以助推。</p>
</li>
<li>
<p>自己开的大会员有<code v-pre>4</code>次助推机会，被赠予的大会员有<code v-pre>1</code>次助推机会，通过赠品活动获得的大会员无法助推。</p>
</li>
<li>
<p>管理员可以通过赠品活动（Giveaways）提升助推次数，每抽取<code v-pre>1</code>个大会员，频道/群组将获得<code v-pre>4</code>个助推。每抽取<code v-pre>500</code>个星币，频道/群组将获得<code v-pre>1</code>个助推。</p>
<details class="hint-container details"><summary>界面截图</summary>
<figure><img src="https://cdn.jsdelivr.net/gh/tgwiki/images/tfa/boost2.jpg" alt="tfa-boost2.jpg" tabindex="0" loading="lazy"><figcaption>tfa-boost2.jpg</figcaption></figure>
</details>
</li>
<li>
<p>赠送他人会员，在其有效期内你将获得<code v-pre>3</code>次助推机会。</p>
</li>
<li>
<p>为群组助推后会在消息右上角显示助推标识。</p>
</li>
</ul>
<h2 id="助推规则" tabindex="-1"><a class="header-anchor" href="#助推规则"><span>助推规则</span></a></h2>
<ul>
<li>初始等级为0级。</li>
<li>助推次数每达到频道/群组人数的<code v-pre>0.4%</code>，等级提升<code v-pre>1</code>级，最高为<code v-pre>100</code>级。</li>
<li>等级到达1级每天可发布1条动态，2级每天可发布2条动态，以此类推。</li>
<li>1级可以设置一个自定义表情回应，2级可以设置2个，以此类推。</li>
<li>频道达到<code v-pre>3</code>级，可以为频道开启自动翻译功能。</li>
<li>频道等级达到<code v-pre>5</code>级，可以自定义频道颜色和消息样式。</li>
<li>群组等级到达<code v-pre>4</code>级，可以添加自定义表情包（emoji），成员均可以在群组内使用自定义表情包。</li>
<li>群组等级到达<code v-pre>6</code>级，成员可以在群内免费使用语音转文字。</li>
<li>等级达到<code v-pre>10</code>级，可以自定义频道/群组背景图片并对订阅者展示。</li>
<li>群组可能与频道略有不同，<strong>具体以应用内显示为准</strong>。</li>
</ul>
